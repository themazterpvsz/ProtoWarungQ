public class Contact {

    String id;
    String ns;
    String cs;
    String telcs;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNs() {
        return ns;
    }

    public void setNs(String ns) {
        this.ns = ns;
    }

    public String getCs() {
        return cs;
    }

    public void setCs(String cs) {
        this.cs = cs;
    }

    public String getTelcs() {
        return telcs;
    }

    public void setTelcs(String telcs) {
        this.telcs = telcs;
    }
}
